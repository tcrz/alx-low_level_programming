#include "3-calc.h"

/**
 * op_add - addition of two numbers
 * @a: int
 * @b: int
 * Return: sum of numbers  if successful
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - substracts two numbers
 * @a: int
 * @b: int
 * Return: substraction of numbers if successful
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies two numbers
 * @a: int
 * @b: int
 * Return: muplication of numbers if successful
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides two numbers
 * @a: int
 * @b: int
 * Return: division of numbers if successful
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - remainder of the division two numbers
 * @a: int
 * @b: int
 * Return: remainder of division of numbers if successful
 */

int op_mod(int a, int b)
{
	return (a % b);
}
